    // Countdown 30 menit
    (function () {
      const el = document.getElementById('timer');
      const end = Date.now() + (30 * 60 * 1000);
      const pad = (n) => String(n).padStart(2, '0');
      function tick() {
        const remain = Math.max(0, end - Date.now());
        const totalSec = Math.floor(remain / 1000);
        const mm = Math.floor(totalSec / 60);
        const ss = totalSec % 60;
        el.textContent = `${pad(mm)}:${pad(ss)}`;
        if (remain > 0) requestAnimationFrame(tick);
        else el.textContent = '00:00';
      }
      tick();
    })();

    // Generate ID Transaksi berdasarkan tanggal & waktu
    (function () {
      const el = document.getElementById('trxId');
      const now = new Date();
      const pad = (n) => String(n).padStart(2, '0');
      const y = now.getFullYear();
      const m = pad(now.getMonth() + 1);
      const d = pad(now.getDate());
      const hh = pad(now.getHours());
      const mm = pad(now.getMinutes());
      const ss = pad(now.getSeconds());
      el.textContent = `#INV-${y}${m}${d}-${hh}${mm}${ss}`;
    })();
